#include "libft.h"

int	ft_isalpha(int c)
{
	return (('A' <= c && c <= 'Z') || ('a' <= c && c <= 'z'));
}

// 101 <= c && c <= 132(A<=Z), 141 <= c && c <= 172(a<=z)